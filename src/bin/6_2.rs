use std::{thread::sleep, time::Duration};

#[derive(Debug, PartialEq, Eq)]
enum Direction {
    Up,
    Down,
    Right,
    Left
}
fn test_loop(mut lines: Vec<Vec<char>>, print: bool) -> bool {
    let X = lines[0].len();
    let Y = lines.len();
    let mut guard = (0, 0);
    for x in 0..X {
        for y in 0..Y {
            if lines[y][x] == '^' {
                guard = (x, y);
                lines[y][x] = 'X';
            }
        }
    }
    let base_guard = guard.clone();
    let mut dir = Direction::Up;
    let mut i = 0;
    loop {
        i+=1;
        match dir {
            Direction::Up => if guard.1==0 {
                lines[guard.1][guard.0] = 'X';
                return false;
            } else if lines[guard.1-1][guard.0] == '#' {
                dir = Direction::Right;
            } else {
                lines[guard.1][guard.0] = 'X';
                guard.1 -= 1;
            },
            Direction::Down => if guard.1==Y-1 {
                lines[guard.1][guard.0] = 'X';
                return false;
            } else if lines[guard.1+1][guard.0] == '#' {
                dir = Direction::Left;
            } else {
                lines[guard.1][guard.0] = 'X';
                guard.1 += 1;
            },
            Direction::Right => if guard.0==X-1 {
                lines[guard.1][guard.0] = 'X';
                return false;
            } else if lines[guard.1][guard.0+1] == '#' {
                dir = Direction::Down;
            } else {
                lines[guard.1][guard.0] = 'X';
                guard.0 += 1;
            },
            Direction::Left => if guard.0==0 {
                lines[guard.1][guard.0] = 'X';
                return false;
            } else if lines[guard.1][guard.0-1] == '#' {
                dir = Direction::Up;
            } else {
                lines[guard.1][guard.0] = 'X';
                guard.0 -= 1;
            }
        }
        if guard == base_guard && dir == Direction::Up {
            return true;
        }
        if i > X*Y {
            return true;
        }
    }
}

fn main() {
    let lines = INPUT.lines().map(|l| l.chars().collect::<Vec<_>>()).collect::<Vec<_>>();
    let X = lines[0].len();
    let Y = lines.len();
    let mut count: usize = 0;
    for x in 0..X {
        for y in 0..Y {
            println!("{}, {}", x, y);
            if lines[y][x] != '.' {
                continue
            }
            let mut new_lines = lines.clone();
            new_lines[y][x] = '#';
            if test_loop(new_lines, x==3 && y==26) {
                count += 1;
            }
        }
    }
    dbg!(count);
}

